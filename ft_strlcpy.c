#include "libft.h"

size_t			ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t		srcsize;
	size_t		i;

	if (!dst && !src)
		return (0);
	srcsize = ft_strlen(src);
	i = 0;
	while (i < srcsize && i + 1 < dstsize)
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize > 0)
		dst[i] = 0;
	return (srcsize);
}
